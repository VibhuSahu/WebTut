// Performing a task
function greet(name, lastName) {
    console.log(`Hello ${name} ${lastName}`);
}

// Calculating a value
function square(number) {
    return number * number;
}

greet('Vibhu', 'Sahu');                     // Output : Hello Vibhu Sahu
console.log("Square of 5 is " + square(5)); // Output : Square of 5 is 25